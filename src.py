class Defenitor:

    def __init__(self, start_value=0):
        self.value = start_value


    def add_3(self):
        self.value += 3


    def add_2(self):
        self.value += 2


    def add_1(self):
        self.value += 1


    def get_value(self):
        return self.value


    def set_to_zero(self):
        self.value = 0
